package reply;

public class Reply {

	private int qna_qna_no;
	private int rep_no;
	private String rep_content;
	private String rep_date;
	public int getQna_qna_no() {
		return qna_qna_no;
	}
	public void setQna_qna_no(int qna_qna_no) {
		this.qna_qna_no = qna_qna_no;
	}
	public int getRep_no() {
		return rep_no;
	}
	public void setRep_no(int rep_no) {
		this.rep_no = rep_no;
	}
	public String getRep_content() {
		return rep_content;
	}
	public void setRep_content(String rep_content) {
		this.rep_content = rep_content;
	}
	public String getRep_date() {
		return rep_date;
	}
	public void setRep_date(String rep_date) {
		this.rep_date = rep_date;
	}
}
